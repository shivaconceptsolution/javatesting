class Multipleif
{
    public static void main(String args[])
   {
      int num=90;
      if(num%3==0)
      {
           System.out.println("divisible by 3");
      }
       if(num%5==0)
      {
           System.out.println("divisible by 5");
      }
       if(num%9==0)
      {
           System.out.println("divisible by 9");
      }
   }


}